const btnSortear = document.getElementById('btnSortear');
const telaInicial = document.getElementById('telaInicial');
const areaEnvelope = document.getElementById('areaEnvelope');
const areaCarta = document.getElementById('areaCarta');
const btnOutro = document.getElementById('btnOutro');

let nivelAtual = ''; // Guarda o nível sorteado

// ✅ Função corrigida: evento de clique no envelope já adicionado na criação
function sortearDesafio() {
    // Limpa tudo
    areaEnvelope.innerHTML = '';
    areaCarta.style.display = 'none';
    btnOutro.style.display = 'none';
    let podeAbrir = false; // Controla quando pode abrir o envelope

    // Sorteia o nível
    const niveis = ['facil', 'medio', 'dificil', 'insano'];
    nivelAtual = niveis[Math.floor(Math.random() * niveis.length)];

    // Cria o envelope
    const envelope = document.createElement('div');
    envelope.className = `envelope ${nivelAtual}`;
    envelope.textContent = '✉️';
    areaEnvelope.appendChild(envelope);

    // ✅ Evento de clique já adicionado aqui (não espera a animação acabar)
    envelope.addEventListener('click', () => {
        if (!podeAbrir) return; // Bloqueia se ainda estiver caindo
        abrirEnvelope();
    });

    // Depois que terminar de cair, libera para abrir
    setTimeout(() => { podeAbrir = true; }, 1350);
}

// ✅ Função separada para abrir o envelope
function abrirEnvelope() {
    const lista = desafios[nivelAtual];
    const desafioEscolhido = lista[Math.floor(Math.random() * lista.length)];

    let nomeNivel, corBorda;
    if(nivelAtual === 'facil') { nomeNivel = '🟢 FÁCIL'; corBorda = '#22c55e'; }
    else if(nivelAtual === 'medio') { nomeNivel = '🟡 MÉDIO'; corBorda = '#eab308'; }
    else if(nivelAtual === 'dificil') { nomeNivel = '🔴 DIFÍCIL'; corBorda = '#ef4444'; }
    else { nomeNivel = '⚫ INSANO'; corBorda = '#ffffff'; }

    areaCarta.style.display = 'block';
    areaCarta.innerHTML = `
        <div class="carta" style="border-color: ${corBorda}">
            <h3>${nomeNivel}</h3>
            <p>${desafioEscolhido}</p>
        </div>
    `;

    btnOutro.style.display = 'block';
    areaEnvelope.innerHTML = ''; // ✅ Envelope some ao abrir
}

// Evento do botão Sortear
btnSortear.addEventListener('click', () => {
    telaInicial.style.opacity = '0';
    setTimeout(() => {
        telaInicial.style.display = 'none';
        sortearDesafio();
    }, 400);
});

// Evento do botão Outro Desafio
btnOutro.addEventListener('click', sortearDesafio);
